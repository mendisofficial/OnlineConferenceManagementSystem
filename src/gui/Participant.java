    private void loadParticipant() {
        try {
            ResultSet result = MySQL.executeSearch("SELECT * FROM `participants` INNER JOIN `genders` "
                    + "ON `participants`.`gender_id` = `genders`.`gender_id`");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (result.next()) {
                Vector vector = new Vector();
                vector.add(result.getString("participant_id"));
                vector.add(result.getString("full_name"));
                vector.add(result.getString("email"));
                vector.add(result.getString("contact_number"));
                vector.add(result.getString("genders.name"));
                vector.add(result.getString("participant_type"));

                dtm.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void selectParticipant() {
        try {
            ResultSet result = MySQL.executeSearch("SELECT * FROM `participants` INNER JOIN `genders` "
                    + "ON `participants`.`gender_id` = `genders`.`gender_id`");

            Vector<String> vector = new Vector<>();
            vector.add("Select Participant");
            while (result.next()) {
                vector.add(result.getString("email"));

                Participants participants = new Participants();
                participants.setParticipant_id(result.getInt("participant_id"));
                participants.setFull_name(result.getString("full_name"));
                participants.setEmail(result.getString("email"));
                participants.setContact_number(result.getString("contact_number"));
                participants.setGender(result.getString("genders.name"));
                participants.setParticipant_type(result.getString("participant_type"));

                participantMap.put(result.getString("email"), participants);
            }

            DefaultComboBoxModel dcbm = new DefaultComboBoxModel(vector);
            jComboBox2.setModel(dcbm);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

