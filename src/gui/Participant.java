    private void jComboBox2ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox2ItemStateChanged
        // Select Participant combo box

        String participantEmail = String.valueOf(jComboBox2.getSelectedItem());

        if (!participantEmail.equals("Select Participant")) {
            Participants p = participantMap.get(participantEmail);

            jTextField1.setText(p.getFull_name());
            jTextField2.setText(p.getEmail());
            jTextField3.setText(p.getContact_number());
            if (p.getGender().equals("Male")) {
                jRadioButton1.setSelected(true);
            } else {
                jRadioButton2.setSelected(true);
            }
            jComboBox1.setSelectedItem(p.getParticipant_type());

        }
    }//GEN-LAST:event_jComboBox2ItemStateChanged

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Update Participant Button

        String fullName = jTextField1.getText();
        String email = jTextField2.getText();
        String contactNumber = jTextField3.getText();
        String type = String.valueOf(jComboBox1.getSelectedItem());

        ButtonModel gender = buttonGroup1.getSelection();
        String gender_id = gender.getActionCommand();

        try {
            MySQL.executeIUD("UPDATE `participants` SET `full_name` = '" + fullName + "', `contact_number` = '" + contactNumber + "', `gender_id` = '" + gender_id + "', `participant_type` = '" + type + "' WHERE `email` = '" + email + "';");
            JOptionPane.showConfirmDialog(this, "Successfully Updated", "Success", JOptionPane.PLAIN_MESSAGE);

            loadParticipant();
            selectParticipant();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

