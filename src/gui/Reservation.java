    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Search button

        String searchText = jTextField3.getText();

        try {
            ResultSet result = MySQL.executeSearch("SELECT * FROM reservations INNER JOIN participants ON reservations.participant_id = participants.participant_id "
                    + "WHERE participants.participant_id LIKE '%" + searchText + "%' OR participants.full_name LIKE '%" + searchText + "%'");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (result.next()) {
                Vector<String> row = new Vector<>();
                row.add(result.getString("reservation_id"));
                row.add(result.getString("participant_id"));
                row.add(result.getString("event_id"));
                row.add(result.getString("reservation_date"));
                row.add(result.getString("expiration_date"));
                dtm.addRow(row);
            }

            if (dtm.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "No results found", "Search", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

