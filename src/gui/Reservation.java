    private void loadParticipantsAndEvents() {
        try {
            // Load Participant IDs
            ResultSet participants = MySQL.executeSearch("SELECT participant_id FROM participants ORDER BY participant_id");
            Vector<String> participantVector = new Vector<>();
            participantVector.add("Select Participant");
            while (participants.next()) {
                participantVector.add(participants.getString("participant_id"));
            }
            jComboBox1.setModel(new DefaultComboBoxModel<>(participantVector));

            // Load Event IDs
            ResultSet events = MySQL.executeSearch("SELECT event_id FROM events ORDER BY event_id");
            Vector<String> eventVector = new Vector<>();
            eventVector.add("Select Event");
            while (events.next()) {
                eventVector.add(events.getString("event_id"));
            }
            jComboBox2.setModel(new DefaultComboBoxModel<>(eventVector));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadReservations() {
        try {
            ResultSet result = MySQL.executeSearch("SELECT * FROM reservations "
                    + "INNER JOIN participants ON reservations.participant_id = participants.participant_id "
                    + "INNER JOIN events ON reservations.event_id = events.event_id");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (result.next()) {
                Vector<String> row = new Vector<>();
                row.add(result.getString("reservation_id"));
                row.add(result.getString("participants.full_name"));
                row.add(result.getString("events.title"));
                row.add(result.getString("reservation_date"));
                row.add(result.getString("expiration_date"));
                dtm.addRow(row);
            }

            // Load Reservation IDs into ComboBox
            ResultSet reservations = MySQL.executeSearch("SELECT reservation_id FROM reservations");
            Vector<String> reservationVector = new Vector<>();
            reservationVector.add("Select Reservation");
            while (reservations.next()) {
                reservationVector.add(reservations.getString("reservation_id"));
            }
            jComboBox3.setModel(new DefaultComboBoxModel<>(reservationVector));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadReservationDetails(String reservationId) {
        try {
            ResultSet result = MySQL.executeSearch("SELECT * FROM reservations WHERE reservation_id = '" + reservationId + "'");

            if (result.next()) {
                jComboBox1.setSelectedItem(result.getString("participant_id"));
                jComboBox2.setSelectedItem(result.getString("event_id"));
                jTextField1.setText(result.getString("reservation_date"));
                jTextField2.setText(result.getString("expiration_date"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

